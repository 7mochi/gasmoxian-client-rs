///
///  Field         Bits   Offset     Description
///  _msg_type     4      byte 0:0   ClientMessage::EndRace
///  _pad0         4      byte 0:4   Unused
///  _pad1         24     byte 1:0   Unused
///  course_time   32     byte 4:0   Race time in PS1 ticks (i32 LE)
///  lap_time      32     byte 8:0   Best lap in PS1 ticks (i32 LE)
use deku::prelude::*;

use crate::protocol::ClientMessage;

#[derive(Debug, Clone, Copy, Default, DekuRead, DekuWrite)]
pub struct EndRace {
    #[deku(bits = "4", ctx = "deku::ctx::Order::Lsb0")]
    _msg_type: u8,

    #[deku(pad_bits_after = "4")]
    _pad0: (),

    #[deku(pad_bytes_after = "3")]
    _pad1: (),

    #[deku(endian = "little")]
    pub course_time: i32,

    #[deku(endian = "little")]
    pub lap_time: i32,
}

impl EndRace {
    /// Creates a race result message.
    ///
    /// `course_time` and `lap_time` are in PS1 ticks (milliseconds).
    pub fn new(course_time: i32, lap_time: i32) -> Self {
        Self {
            _msg_type: ClientMessage::EndRace as u8,
            course_time,
            lap_time,
            ..Default::default()
        }
    }
}
