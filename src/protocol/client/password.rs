///
///  Field      Bits   Offset     Description
///  _msg_type   8      byte 0:0  ClientMessage::Password
///  sequence   64     byte 1:0   Password entry (8 bytes)
use deku::{DekuRead, DekuWrite};

use crate::protocol::ClientMessage;

#[derive(Debug, Clone, Copy, DekuRead, DekuWrite)]
pub struct Password {
    _msg_type: u8,

    pub sequence: [u8; 8],
}

impl Password {
    /// Creates a password entry message with the 8-byte sequence
    /// read from shared memory.
    pub fn new(sequence: [u8; 8]) -> Self {
        Self {
            _msg_type: ClientMessage::Password as u8,
            sequence,
        }
    }
}
